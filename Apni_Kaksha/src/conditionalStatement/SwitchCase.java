package conditionalStatement;

public class SwitchCase {

	public static void main(String[] args) {
		int a=1;
		switch (a) {
		case 1:
			
		case 2:
		{
			System.out.println("i am on leave");
			break;
		}
		case 3:
		{
			System.out.println("I am in office");
		}

		default:
			System.out.println("Input a valid number");
		
		}
		
	}

}
